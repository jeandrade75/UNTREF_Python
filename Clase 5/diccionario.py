# Diccionario
estudiante = {
    "nombre": "Juan C",
    "edad": 21,
    "carrera": "Ingeniería"
}
print("\nDiccionario del estudiante:", estudiante)

# Acceder a un valor en el diccionario
print("Nombre del estudiante:", estudiante["nombre"])

# Modificar un valor en el diccionario
estudiante["edad"] = 22
print("Diccionario después de cambiar la edad:", estudiante)